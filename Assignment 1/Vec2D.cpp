#include "Vec2D.h"

/******************************************************************************/
/*!
\brief
Default parameter. Setting values to 0
*/
/******************************************************************************/
Vec2D::Vec2D()
{
    x = 0;
    y = 0;
}

/******************************************************************************/
/*!
\brief
Set the elements of this vector

\param  x
x value
\param  y
y value
*/
/******************************************************************************/
Vec2D::Vec2D(double x, double y)
{
    this->x = x;
    this->y = y;
}